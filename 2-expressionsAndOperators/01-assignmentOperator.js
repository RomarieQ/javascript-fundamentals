// Assignment Operator
let r = 5; // = will assign a value, initialize


// Addition assignment operator
r = r + 1; //longhand
r += 1; // shorthand add 1 and re assign to r
r++; // shorterhand, but can only go up by 1


// Subtraction assignment operator
r -= 7;
r--;


// Multiplication assignment operator
r *= 10;


// Division assignment operator
r /= 2;


// Exponents assignment operator
r **= 3;
3 ** 2 // is 3 squared


// Modulus assignment operator
r %= 5; // gives remainder divided by that number. Hint: number is even if you get 0